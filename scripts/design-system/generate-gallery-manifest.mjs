#!/usr/bin/env node
/**
 * Gallery manifest generator (RFC §6, ratified decision #7).
 *
 * Scans src/components/atoms/<Atom>/gallery.tsx and (re)writes
 * src/components/atoms/galleryManifest.ts — a generated, committed file
 * that imports every atom's `gallery` export and exposes them as one
 * `galleryManifest` array consumed by the /playground route and the
 * design-system/gallery-manifest-fresh lint rule (eslint.config.local.ts).
 *
 * Deterministic: atoms are emitted in alphabetical order, so the output
 * is stable across machines and reruns.
 *
 * Usage: node scripts/design-system/generate-gallery-manifest.mjs
 */
import { existsSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..", "..");
export const ATOMS_DIR = join(ROOT, "src", "components", "atoms");
export const MANIFEST_PATH = join(ATOMS_DIR, "galleryManifest.ts");

/**
 * Lists atom directory names (alphabetical) under src/components/atoms.
 *
 * @param {string} atomsDir absolute path to the atoms directory
 * @returns {string[]} sorted directory names, excluding __tests__
 */
export function listAtomDirs(atomsDir) {
  return readdirSync(atomsDir)
    .filter(name => name !== "__tests__")
    .filter(name => statSync(join(atomsDir, name)).isDirectory())
    .sort((a, b) => (a < b ? -1 : a > b ? 1 : 0));
}

/**
 * Lists atom directories that ship a gallery.tsx (alphabetical).
 *
 * @param {string} atomsDir absolute path to the atoms directory
 * @returns {string[]} sorted atom names with a gallery.tsx
 */
export function listGalleryAtoms(atomsDir) {
  return listAtomDirs(atomsDir).filter(name =>
    existsSync(join(atomsDir, name, "gallery.tsx"))
  );
}

/**
 * Derives the import alias for an atom's gallery export (Box → boxGallery).
 *
 * @param {string} name atom directory name (PascalCase)
 * @returns {string} camelCase import alias
 */
function galleryAlias(name) {
  return `${name.charAt(0).toLowerCase()}${name.slice(1)}Gallery`;
}

/**
 * Builds the full source text of galleryManifest.ts for the given atoms.
 *
 * @param {string[]} atoms sorted atom names that have a gallery.tsx
 * @returns {string} TypeScript source for the generated manifest file
 */
export function buildManifestSource(atoms) {
  const header = [
    "/**",
    " * GENERATED by scripts/design-system/generate-gallery-manifest.mjs — do not edit.",
    " * @file galleryManifest.ts",
    " * @description Generated manifest of every atom gallery entry, in",
    " * alphabetical order. Consumed by the /playground route and the",
    " * design-system/gallery-manifest-fresh lint rule (eslint.config.local.ts).",
    " * Regenerate with: node scripts/design-system/generate-gallery-manifest.mjs",
    " * @module components/atoms/galleryManifest",
    " */",
  ].join("\n");

  const galleryImports = atoms
    .map(
      name =>
        `import { gallery as ${galleryAlias(name)} } from "./${name}/gallery";`
    )
    .join("\n");

  const typeImport = 'import type { GalleryEntry } from "./galleryTypes";';

  const imports = [galleryImports, typeImport].filter(Boolean).join("\n\n");

  const entries = atoms.map(name => `  ${galleryAlias(name)},`).join("\n");
  const manifest =
    atoms.length === 0
      ? "export const galleryManifest: readonly GalleryEntry[] = [];"
      : `export const galleryManifest: readonly GalleryEntry[] = [\n${entries}\n];`;

  return `${header}\n${imports}\n\n${manifest}\n`;
}

/**
 * Generates and writes galleryManifest.ts, logging the result.
 */
function main() {
  const atoms = listGalleryAtoms(ATOMS_DIR);
  writeFileSync(MANIFEST_PATH, buildManifestSource(atoms));
  console.log(
    `Wrote ${MANIFEST_PATH} with ${atoms.length} gallery entr${atoms.length === 1 ? "y" : "ies"}: ${atoms.join(", ") || "(none)"}`
  );
}

if (
  process.argv[1] &&
  import.meta.url === pathToFileURL(process.argv[1]).href
) {
  main();
}
